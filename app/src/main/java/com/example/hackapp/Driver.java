package com.example.hackapp;

public class Driver {
    public String name;

    public String start;

    public String destination;

    public String time;

    public String date;

    public boolean repeat;

    public Driver(String name, String start, String destination, String time, String date, boolean repeat){
        this.name = name;
        this.start = start;
        this.destination = destination;
        this.time = time;
        this.date = date;
        this.repeat = repeat;
    }

}
